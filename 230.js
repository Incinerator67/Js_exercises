let now  = new Date(2015, 2, 15);
let date = new Date(2000, 9, 1);

let diff = now - date; // вычитаем два объекта с датами друг от друга
console.log(diff);     // увидим разницу в миллисекундах
console.log(diff/(1000 * 60 * 60 * 24)); // увидим разницу в днях
console.log(diff/(1000 * 60 * 60 * 24 * 12)); // увидим разницу в месяцах
console.log(diff/(1000 * 60 * 60 * 24 * 365)); // увидим разницу в годах